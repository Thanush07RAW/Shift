public class Shift {
    public static void main(String[] args) {
        int a = 8;
        int result = a << 9;
        System.out.printf("The left shift of %d is: %d", a, result);

        int b = 17;
        int res = b >> 9; 
        System.out.printf("\nThe right shift of %d is : %d", b, res); 
    }
}

